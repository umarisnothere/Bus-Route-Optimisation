vector<int> dijkstra(int s, int d){
	
	 priority_queue<pair<double, int>, vector<pair<double, int>>, greater<pair<double, int>>> pq;
	 
	 pq.push({0,s});
	 for(int i=0;i<1000;i++){
	 	parent[i]=i;
	 }

	 for(int i=0;i<1000;i++){
	 	dist[i]=INF;
	 }
	 dist[s]=0;
	 while(!pq.empty()){
	 	auto curr = pq.top();
	 	pq.pop();

	 	int curr_node = curr.second;
	 	double curr_dist = curr.first;
	 	if(vis[curr_node]) continue;
	 	vis[curr_node]=1;

	 	for(auto child:g[curr_node]){

	 		int child_node = child.first;
	 		double child_wt = child.second.first;
	 		if(dist[child_node]>dist[curr_node]+child_wt){

	 			dist[child_node]=dist[curr_node]+child_wt;
	 			pq.push({dist[child_node],child_node});
	 			parent[child_node]=curr_node;
	 			bus[child_node] = child.second.second;
	 		}
	 	}
	 }

	 if(dist[d]==INF) return{-1};
	 vector<int> path;
	 int node = d;
	 while(parent[node]!=node){
	 	path.push_back(node);

	 	node=parent[node];
	 }

	 reverse(path.begin(),path.end());
	 return path;
}


